public class Calculator {

    public int addFunction(int num1, int num2) {
        return num1 + num2;
    }

    public int subtractFunction(int num1, int num2) {
        return num1 - num2;
    }

    public int multiplyFunction(int num1, int num2) {
        return num1 * num2;
    }

    public double divideFunction(double num3, double num4) {
        return num3 / num4;
    }
}
